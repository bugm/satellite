package satellite;

public class satelliteInfo {
	private String id;
	private String name;
	private double weight;
	private double volume;
	private int worktime;
	private int epoch;
	private double angle;
	private double xnode;
	private double eo;
	private double omega;
	private double xmo;
	private double xno;
	
	public String getid()
	{
		return id;
	}
	public void setId(String id)
	{
		this.id = id;
	}
	public String getname()
	{
		return name;
	}
    public void setName(String name)
    {
    	this.name = name;
    }
    public double getWeight()
    {
    	return weight;
    }
    public void setWeight(double weight)
    {
    	this.weight = weight;
    }
    public double getVolume()
    {
    	return volume;
    }
    public void setVolume(double volume)
    {
    	this.volume = volume;
    }
    public int getWorktime()
    {
    	return worktime;
    }
    public void setWorktime(int time)
    {
    	this.worktime = time;
    }
    public int getEpoch()
    {
    	return epoch;
    }
    public void setEpoch(int epoch)
    {
    	this.epoch = epoch;
    }
    public double getAngle()
    {
    	return angle;
    }
    public void setAngle(double angle)
    {
    	this.angle = angle;
    }
    public double getXnode()
    {
    	return xnode;
    }
    public void setXnode(double xnode)
    {
    	this.xnode = xnode;
    }
    public double getEo()
    {
    	return eo;
    }
    public void setEo(double eo)
    {
    	this.eo = eo;
    }
    public double getOmega()
    {
    	return omega;
    }
    public void setOmega(double omega)
    {
    	this.omega = omega;
    }   
    public double getXno()
    {
    	return xno;
    }
    public void setXno(double xno)
    {
    	this.xno = xno;
    }
    public double getXmo()
    {
    	return xmo;
    }
    public void setXmo(double xmo)
    {
    	this.xmo = xmo;
    }
    
    
}
